/** リザルト画面に添える物語の結末 */
export function epilogueText(won: boolean, timeSec: number): string {
  if (won) {
    return '家族はとっくに森を抜けた。勇者たちの心は折れた。——今夜は、新しい村で肉を焼こう。';
  }
  const m = Math.floor(timeSec / 60);
  const s = Math.floor(timeSec % 60);
  const t = m > 0 ? `${m}分${s}秒` : `${s}秒`;
  return `オークは倒れた。だが、彼が稼いだ${t}のあいだに、家族は森の奥へ。——その背中を、子どもたちは忘れない。`;
}
